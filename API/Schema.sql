"AiringDate" NUMERIC NULL,
"StatusId" INTEGER NULL,
"TypeId" INTEGER NULL,
"SeasonId" INTEGER NULL, EnglishTitle TEXT NULL,
FOREIGN KEY(StatusId) REFERENCES AnimeStatus(Id) ON DELETE SET NULL,
FOREIGN KEY(TypeId) REFERENCES AnimeType(Id) ON DELETE SET NULL,
FOREIGN KEY(SeasonId) REFERENCES Season(Id) ON DELETE SET NULL);
