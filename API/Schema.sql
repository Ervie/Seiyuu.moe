"Id" INTEGER NOT NULL CONSTRAINT "PK_Role" PRIMARY KEY AUTOINCREMENT,
"AnimeId" INTEGER NULL,
"RoleTypeId" INTEGER NULL,
"CharacterId" INTEGER NULL,
"SeiyuuId" INTEGER NULL,
"LanguageId" INTEGER NULL,
FOREIGN KEY(AnimeId) REFERENCES Anime(MalId) ON DELETE CASCADE,
